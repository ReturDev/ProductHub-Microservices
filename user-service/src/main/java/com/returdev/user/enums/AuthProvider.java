package com.returdev.user.enums;

/**
 * Enum representing the different authentication providers supported in the system.
 * This enum is used to identify the provider through which a user is authenticated.
 *<p>
 * Currently, the system supports the following authentication providers:
 * - GOOGLE
 * - GITHUB
 */
public enum AuthProvider {

    /**
     * Represents authentication through Google.
     */
    GOOGLE,

    /**
     * Represents authentication through GitHub.
     */
    GITHUB

}

